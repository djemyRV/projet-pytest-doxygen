"""
@file calculator.py
@brief This module provides basic arithmetic operations.
"""

def add(a, b):
    """
    Adds two numbers.

    @param a First number
    @param b Second number
    @return The sum of a and b
    """
    return a + b

def subtract(a, b):
    """
    Subtracts the second number from the first.

    @param a First number
    @param b Second number
    @return The difference between a and b
    """
    return a - b
